class ShitsAndGiggles:
    def __init__(self, num_of_shits=1, num_of_giggles=1):
        self.num_of_shits = num_of_shits
        self.num_of_giggles = num_of_giggles

    def get_num_of_shits(self):
        return self.num_of_shits

    def get_num_of_giggles(self):
        return self.num_of_giggles
